CREATE TABLE IF NOT EXISTS student_group (
groupId BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
groupNumber INT NOT NULL,
groupFaculty VARCHAR(255) NOT NULL,
UNIQUE (groupNumber));


CREATE TABLE IF NOT EXISTS student (
studentId BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
studentName VARCHAR(255) NOT NULL,
studentSurname VARCHAR(255) NOT NULL,
studentPatronymic VARCHAR(255) NOT NULL,
studentDateOfBirth DATE NOT NULL,
studentGroupNumber INT NOT NULL,
FOREIGN KEY (studentGroupNumber) REFERENCES student_group (GroupNumber) ON UPDATE CASCADE);